#!/usr/bin/env node
/**
 * Build-time helper: snapshot every directory in the monorepo's `examples/`
 * tree into `packages/create-act-app/templates/<name>/`, plus a
 * `templates/manifest.json` describing the available templates. The runtime
 * bin reads from this snapshot — at publish time the package tarball ships
 * the snapshot rather than fetching from GitHub at runtime (Approach A from
 * the runbook; Approach B / network fetch is deferred per §6.40).
 *
 * Excludes per-example build artefacts (`node_modules`, `dist`, `_site`,
 * `.next`, `.act-build-report.json`, `tsconfig.tsbuildinfo`, etc.) — anything
 * regenerated by the user's first `pnpm install && pnpm build`.
 */
import { promises as fs, existsSync } from 'node:fs';
import * as path from 'node:path';
import * as url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const PKG_ROOT = path.resolve(__dirname, '..');
const REPO_ROOT = path.resolve(PKG_ROOT, '..', '..');
const EXAMPLES_DIR = path.join(REPO_ROOT, 'examples');
const TEMPLATES_DIR = path.join(PKG_ROOT, 'templates');

/** Directory / file names skipped during the recursive copy. */
const EXCLUDED_NAMES = new Set([
  'node_modules',
  'dist',
  '_site',
  '.next',
  '.nuxt',
  '.astro',
  '.docusaurus',
  'build',
  'coverage',
  '.tsbuildinfo',
  'tsconfig.tsbuildinfo',
  '.act-build-report.json',
  '.DS_Store',
  '.turbo',
]);

async function rmrf(target) {
  if (!existsSync(target)) return;
  await fs.rm(target, { recursive: true, force: true });
}

async function copyDir(src, dst) {
  await fs.mkdir(dst, { recursive: true });
  const entries = await fs.readdir(src, { withFileTypes: true });
  for (const entry of entries) {
    if (EXCLUDED_NAMES.has(entry.name)) continue;
    const s = path.join(src, entry.name);
    const d = path.join(dst, entry.name);
    if (entry.isDirectory()) {
      await copyDir(s, d);
    } else if (entry.isFile()) {
      await fs.copyFile(s, d);
    }
    // symlinks intentionally skipped — examples shouldn't ship any
  }
}

async function readPackageJsonDescription(dir) {
  const pj = path.join(dir, 'package.json');
  if (!existsSync(pj)) return null;
  const raw = await fs.readFile(pj, 'utf8');
  const parsed = JSON.parse(raw);
  return typeof parsed.description === 'string' ? parsed.description : null;
}

async function main() {
  if (!existsSync(EXAMPLES_DIR)) {
    throw new Error(`build-templates: examples dir missing at ${EXAMPLES_DIR}`);
  }
  await rmrf(TEMPLATES_DIR);
  await fs.mkdir(TEMPLATES_DIR, { recursive: true });

  const entries = await fs.readdir(EXAMPLES_DIR, { withFileTypes: true });
  const manifest = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (EXCLUDED_NAMES.has(entry.name)) continue;
    const src = path.join(EXAMPLES_DIR, entry.name);
    const dst = path.join(TEMPLATES_DIR, entry.name);
    await copyDir(src, dst);
    const description = (await readPackageJsonDescription(src)) ?? '';
    manifest.push({ name: entry.name, description });
    process.stdout.write(`  copied example: ${entry.name}\n`);
  }
  manifest.sort((a, b) => a.name.localeCompare(b.name));
  await fs.writeFile(
    path.join(TEMPLATES_DIR, 'manifest.json'),
    JSON.stringify({ templates: manifest }, null, 2) + '\n',
    'utf8',
  );
  process.stdout.write(
    `build-templates: snapshotted ${manifest.length} example(s) into templates/\n`,
  );
}

main().catch((err) => {
  process.stderr.write(`build-templates: ${err instanceof Error ? err.message : String(err)}\n`);
  process.exit(1);
});
